#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*x;

	if (!lst || !f)
		return ;
	x = lst;
	while (x)
	{
		(*f)(x ->content);
		x = x ->next;
	}
}
